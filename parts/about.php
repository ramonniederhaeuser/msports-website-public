      Warum? Ich wollte Erfolg und hatte Ziele. Ich trainierte unter der Woche hart und viel. 
      Dabei genügte es mir nicht, nur Motorrad zu fahren, ich wollte mich auch körperlich weiterentwickeln. 
      Wie ich meine Leistungsfähigkeit optimieren kann, faszinierte mich schon immer. 
      Mit dieser Motivation im Hintergrund begann ich, meine ersten, 
      spezifisch auf mich und meine Bedürfnisse zugeschnittenen Trainingsprogramme zu erstellen. 
      Stetiges Experimentieren und immerzu neue Übungen, Variationen und Reize brachten mir den gewünschten Erfolg.
      2017 kamen zwei Motocross-Freunde auf mich zu. Sie wollten in der Off-Season etwas Kraft zulegen 
      und gleichzeitig ihre Kondition verbessern. 
      Wir begannen also mit den ersten Trainings in einer Werkstatthalle, anfangs noch zu dritt. 
      Das Interesse weiterer Freunde war geweckt, die Anfragen stiegen und bald wurde die Werkstatt zu klein. 
      Daraufhin organisierte ich eine Turnhalle, in der wir zweimal wöchentlich Kraft und Ausdauer trainierten.
      Der Erfolg meiner Freunde sowie meine Erkenntnis, dass ich Menschen etwas Gutes tun kann, 
      indem ich ihnen dabei helfe, sportliche Ziele zu erreichen, führte schliesslich dazu, 
      dass ich mich Ende 2018 entschied, eine eigene Trainingshalle aufzubauen. 
      Mit der passenden Location in der Schuhfabrik Märwil wurde dieser Wunsch Realität. 
      Nach einigen Wochen Einrichten ist es nun geschafft. 
      MsportS ist eröffnet und bereit, zusammen mit dir Spass am Sport zu haben. 
      Meine Freude hole ich bei meinen Athleten und Freunden. Wenn ich sehe, 
      wie sich ihr Fleiss und die harte Arbeit an Wettkämpfen oder generell im Leben bezahlt machen, 
      habe ich mein Ziel als Coach erreicht.</p>
